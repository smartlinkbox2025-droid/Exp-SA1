// ─── PDF – Multi Packing List ─────────────────────────────────────────────────

export async function exportMultiPackingListPDF(lists: PackingList[]) {
  const doc = new jsPDF({ orientation: "landscape", unit: "mm", format: "a4" });
  await loadCairo(doc);
  const pageW = doc.internal.pageSize.getWidth();
  const pageH = doc.internal.pageSize.getHeight();

  let y = drawHeader(
    doc,
    "Combined Packing List Report",
    `China Import Tracker  |  ${ar("بيانات التعبئة المجمّعة")}  |  Shipments: ${lists.length}  |  ${todayStr()}`,
    32,
  );

  for (const [idx, list] of lists.entries()) {
    const totals = calculatePackingListTotals(list.items);
    if (y > pageH - 60) { doc.addPage(); y = 10; }

    doc.setFillColor(30, 64, 175);
    doc.rect(0, y, pageW, 8, "F");
    doc.setTextColor(255, 255, 255);
    doc.setFont("Cairo", "bold"); doc.setFontSize(8.5);
    doc.text(
      `[${idx + 1}]  ${ar(list.supplierName)}   |   B/L: ${list.billOfLading}   |   ${ar(list.goodsDescription)}`,
      6, y + 5.5
    );
    doc.setTextColor(30, 30, 30);
    y += 10;

    autoTable(doc, {
      startY: y,
      head: [["#", "Product", "Cartons", "Units/Ctn", "NW/Ctn kg", "GW/Ctn kg",
              "L×W×H cm", "CBM/Ctn", "Unit USD", "Total USD"]],
      body: [
        ...list.items.map((item, i) => {
          const cbm = (item.length * item.width * item.height) / 1_000_000;
          const totalValue = item.unitValueUSD * item.unitsPerCarton * item.cartonsCount;
          return [
            i + 1, ar(item.productName), item.cartonsCount, item.unitsPerCarton,
            item.netWeightPerCarton, item.grossWeightPerCarton,
            `${item.length}×${item.width}×${item.height}`,
            cbm.toFixed(3), item.unitValueUSD.toFixed(2), totalValue.toFixed(2),
          ];
        }),
        [
          { content: "SUB-TOTAL", colSpan: 2, styles: { fontStyle: "bold", fillColor: [15,23,42], textColor: 255, font: "Cairo" } },
          { content: totals.totalCartons, styles: { fontStyle: "bold", fillColor: [15,23,42], textColor: 255, font: "Cairo" } },
          { content: "", styles: { fillColor: [15,23,42] } },
          { content: `${totals.totalNetWeight.toFixed(2)} kg`, styles: { fontStyle: "bold", fillColor: [15,23,42], textColor: 255, font: "Cairo" } },
          { content: `${totals.totalGrossWeight.toFixed(2)} kg`, styles: { fontStyle: "bold", fillColor: [15,23,42], textColor: 255, font: "Cairo" } },
          { content: "", styles: { fillColor: [15,23,42] } },
          { content: `${totals.totalCBM.toFixed(3)} m³`, styles: { fontStyle: "bold", fillColor: [15,23,42], textColor: 255, font: "Cairo" } },
          { content: "", styles: { fillColor: [15,23,42] } },
          { content: `$ ${totals.totalValueUSD.toFixed(2)}`, styles: { fontStyle: "bold", fillColor: [15,23,42], textColor: 255, font: "Cairo" } },
        ],
      ],
      theme: "striped",
      headStyles: { ...HEAD_BLUE, fontSize: 7.5 },
      alternateRowStyles: ALT_ROW,
      styles: { ...BASE_STYLE, fontSize: 7.5, cellPadding: 2, halign: "center" },
      columnStyles: { 1: { halign: "right", cellWidth: 38 } },
      margin: { left: 6, right: 6 },
    });

    y = (doc as any).lastAutoTable.finalY + 8;
  }

  // Grand totals bar
  const grandCartons = lists.reduce((s, l) => s + calculatePackingListTotals(l.items).totalCartons, 0);
  const grandCBM    = lists.reduce((s, l) => s + calculatePackingListTotals(l.items).totalCBM, 0);
  const grandGW     = lists.reduce((s, l) => s + calculatePackingListTotals(l.items).totalGrossWeight, 0);
  const grandUSD    = lists.reduce((s, l) => s + calculatePackingListTotals(l.items).totalValueUSD, 0);

  if (y > pageH - 25) { doc.addPage(); y = 10; }
  doc.setFillColor(245, 197, 24);
  doc.rect(6, y, pageW - 12, 12, "F");
  doc.setTextColor(15, 23, 42);
  doc.setFont("Cairo", "bold"); doc.setFontSize(8.5);
  doc.text(
    `GRAND TOTAL  —  Shipments: ${lists.length}  |  Cartons: ${grandCartons}  |  ` +
    `CBM: ${grandCBM.toFixed(3)} m³  |  GW: ${grandGW.toFixed(2)} kg  |  Value: $${grandUSD.toFixed(2)}`,
    pageW / 2, y + 7.5, { align: "center" }
  );

  drawFooter(doc);
  doc.save(`CombinedPackingList_${todayStr().replace(/\//g, "-")}.pdf`);
}

