// ─── Font loading ─────────────────────────────────────────────────────────────

let cairoRegB64: string | null = null;
let cairoBoldB64: string | null = null;

async function ttfToB64(url: string): Promise<string> {
  const resp = await fetch(url);
  if (!resp.ok) throw new Error(`Font fetch failed: ${url}`);
  const buf = await resp.arrayBuffer();
  const u8 = new Uint8Array(buf);
  let bin = "";
  const CHUNK = 8192;
  for (let i = 0; i < u8.length; i += CHUNK)
    bin += String.fromCharCode(...u8.subarray(i, i + CHUNK));
  return btoa(bin);
}

async function loadCairo(doc: jsPDF): Promise<void> {
  if (!cairoRegB64)  cairoRegB64  = await ttfToB64("/fonts/Cairo-Regular.ttf");
  if (!cairoBoldB64) cairoBoldB64 = await ttfToB64("/fonts/Cairo-Bold.ttf");
  doc.addFileToVFS("Cairo-Regular.ttf", cairoRegB64);
  doc.addFont("Cairo-Regular.ttf", "Cairo", "normal");
  doc.addFileToVFS("Cairo-Bold.ttf", cairoBoldB64);
  doc.addFont("Cairo-Bold.ttf", "Cairo", "bold");
  doc.setFont("Cairo", "normal");
}

// ─── Shared helpers ───────────────────────────────────────────────────────────

function triggerDownload(blob: Blob, filename: string) {
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url; a.download = filename;
  document.body.appendChild(a); a.click();
  document.body.removeChild(a); URL.revokeObjectURL(url);
}

function todayStr() {
  return new Date().toLocaleDateString("en-GB");
}

/** Always use Western digits in PDFs to avoid Arabic-Indic numeral encoding issues */
function fmt(num: number, decimals = 2): string {
  return num.toLocaleString("en-US", {
    maximumFractionDigits: decimals,
    minimumFractionDigits: decimals,
  });
}

function safeSheetName(raw: string, taken: string[]): string {
  const clean = raw.replace(/[\/\\?*\[\]:]/g, "").trim().slice(0, 28) || "Sheet";
  let name = clean; let i = 2;
  while (taken.includes(name)) name = `${clean.slice(0, 25)} (${i++})`;
  return name;
}

// jsPDF-autotable style presets using Cairo font
const BASE_STYLE = { font: "Cairo", fontSize: 9, cellPadding: 3 };
const HEAD_DARK   = { fillColor: [15, 23, 42]  as [number,number,number], textColor: 255, font: "Cairo", fontStyle: "bold" as const, fontSize: 9 };
const HEAD_NAVY   = { fillColor: [30, 41, 59]  as [number,number,number], textColor: 255, font: "Cairo", fontStyle: "bold" as const, fontSize: 9 };
const HEAD_BLUE   = { fillColor: [30, 64, 175] as [number,number,number], textColor: 255, font: "Cairo", fontStyle: "bold" as const, fontSize: 9 };
const ALT_ROW     = { fillColor: [245, 247, 250] as [number,number,number] };

// Draw a standard header bar and return the Y position below it
function drawHeader(
  doc: jsPDF,
  title: string,
  subtitle: string,
  barH = 28,
  fillColor: [number,number,number] = [15, 23, 42],
): number {
  const pageW = doc.internal.pageSize.getWidth();
  doc.setFillColor(...fillColor);
  doc.rect(0, 0, pageW, barH, "F");
  doc.setTextColor(255, 255, 255);
  doc.setFont("Cairo", "bold"); doc.setFontSize(15);
  doc.text(title, pageW / 2, barH * 0.42, { align: "center" });
  doc.setFont("Cairo", "normal"); doc.setFontSize(9);
  doc.text(subtitle, pageW / 2, barH * 0.78, { align: "center" });
  doc.setTextColor(30, 30, 30);
  return barH + 4;
}

function drawFooter(doc: jsPDF) {
  const pageW = doc.internal.pageSize.getWidth();
  const pageH = doc.internal.pageSize.getHeight();
  doc.setFont("Cairo", "normal"); doc.setFontSize(7.5);
  doc.setTextColor(150, 150, 150);
  doc.text(
    `Generated by China Import Tracker  |  ${ar("مستورد الصين")}  |  ${todayStr()}`,
    pageW / 2, pageH - 5, { align: "center" }
  );
}

